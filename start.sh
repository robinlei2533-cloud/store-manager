#!/bin/bash
# ============================================================
# 门店管家 — 一键启动脚本
# ============================================================

echo "🏪 门店管家 — 启动中..."
echo ""

# 进入项目目录
cd "$(dirname "$0")/frontend"

# 清除可能冲突的 NODE_OPTIONS
export NODE_OPTIONS=

# 检查 node_modules
if [ ! -d "node_modules" ]; then
  echo "📦 首次运行，正在安装依赖..."
  npm install
  echo ""
fi

echo "🚀 启动开发服务器..."
echo ""
echo "============================================"
echo "  浏览器打开: http://localhost:5173"
echo "  登录方式: 输入任意邮箱和密码即可登录"
echo "  数据模式: 本地演示模式（数据保存在浏览器中）"
echo "============================================"
echo ""

npm run dev
